using System;
using System.Collections.Generic;

namespace Voting.Basis.Core.Domain;

public abstract class PoliticalBusiness
{
    public Guid Id { get; set; }

    /// <summary>
    /// Gets or sets the domain of influence responsible for this political business.
    /// </summary>
    public Guid DomainOfInfluenceId { get; set; }

    /// <summary>
    /// Gets or sets the contest that this political business is part of.
    /// </summary>
    public Guid ContestId { get; set; }

    /// <summary>
    /// Gets or sets a value indicating whether this political business is active.
    /// This means that all information has been correctly entered and results can be entered for the political business.
    /// </summary>
    public bool Active { get; set; }

    /// <summary>
    /// Gets or sets a value indicating whether this political business is e-voting approved or not.
    /// If null, then the political business does not support e-voting (no counting circle of the assigned domain of influence has e-voting).
    /// </summary>
    public bool? EVotingApproved { get; set; }

    /// <summary>
    /// Gets or sets the political business number (in german: Gesch√§ftsnummer).
    /// </summary>
    public string PoliticalBusinessNumber { get; set; } = string.Empty;

    public Dictionary<string, string> OfficialDescription { get; set; } = new();

    public Dictionary<string, string> ShortDescription { get; set; } = new();

    /// <summary>
    /// Gets or sets a value indicating whether ballot bundle numbers are automatically generated by the system or if users enter the number manually.
    /// </summary>
    public bool AutomaticBallotBundleNumberGeneration { get; set; }

    /// <summary>
    /// Gets or sets a value indicating whether ballot numbers are automatically generated by the system or if users enter the number manually.
    /// </summary>
    public bool AutomaticBallotNumberGeneration { get; set; }
}
